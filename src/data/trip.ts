export type Tag = { type: 'cost' | 'transit' | 'tip'; text: string };

export type Location = {
  name: string;
  address: string;
  lat: number;
  lng: number;
};

export type Restaurant = Location & { why: string };

export type Block = {
  id: string;
  duration: number;
  title: string;
  details: string;
  type: 'normal' | 'travel' | 'free';
  tags: Tag[];
  // Legacy single-string location. Still read by ScheduleBlock when `locations`
  // and `eating` are both absent. Will be removed after Phase 4 lands.
  location?: string;
  // Discriminator for new rendering paths.
  kind?: 'activity' | 'eat' | 'travel' | 'free';
  // For activity and travel blocks: each place gets its own Google Maps link.
  locations?: Location[];
  // For eating blocks: area label plus curated restaurant picks.
  eating?: {
    area: string;
    areaCenter: { lat: number; lng: number };
    picks: Restaurant[];
  };
};

export type Recommendation = {
  id: string;
  title: string;
  location?: string;
  details: string;
  defaultDuration: number;
  tags: Tag[];
};

export type Day = {
  id: string;
  label: string;
  title: string;
  date: string;
  dateISO: string;
  city: string;
  startTime: string;
  blocks: Block[];
  recommendations: Recommendation[];
  optionalVisits?: Location[];
};

export type TodoItem = { id: string; title: string; desc: string };
export type TodoSection = { id: string; title: string; className: string; items: TodoItem[] };

